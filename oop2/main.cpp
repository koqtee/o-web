#include "MyClass.h"

int main() {
    // Демонстрация работы конструкторов
    MyClass obj1; // Конструктор по умолчанию
    MyClass obj2(10, 5.5); // Конструктор с параметрами
    MyClass obj3(obj2); // Конструктор копирования

    // Вывод объектов
    cout << "Объект 1: "; obj1.print();
    cout << "Объект 2: "; obj2.print();
    cout << "Объект 3 (копия): "; obj3.print();

    // Проверка работы геттеров и сеттеров
    obj1.setA(-5); // Ошибка, не устанавливает отрицательное значение
    obj1.setA(3);
    obj1.setB(7.2);

    cout << "\nОбъект 1 после изменения: ";
    obj1.print();

    return 0;
}
